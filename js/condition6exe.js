let num1, num2;

num1 = parseInt(prompt("수를 입력: "));
num2 = parseInt(prompt("수를 입력: "));
if (num1 > num2) {
  console.log("두수중에서 큰값은 " + num1 + "입니다.")
} else {
  console.log("두수중에서 큰값은 " + num2 + "입니다.")
}
